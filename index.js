// ƒê·ªçc cache c√°c file t·ª´ng c√≥ ·ªü local
function readCache(cacheFile) {
  if (!fs.existsSync(cacheFile)) return [];
  try {
    return JSON.parse(fs.readFileSync(cacheFile, "utf8"));
  } catch (e) {
    return [];
  }
}

// Ghi l·∫°i cache c√°c file local ƒë√£ c√≥ l·∫ßn sync n√†y
function writeCache(cacheFile, files) {
  try {
    fs.writeFileSync(cacheFile, JSON.stringify(files), "utf8");
  } catch (e) {}
}

// ƒê·ªçc danh s√°ch file ƒë√£ ch·ªçn "nn" (kh√¥ng h·ªèi l·∫°i n·ªØa, kh√¥ng t·∫£i n·ªØa)
